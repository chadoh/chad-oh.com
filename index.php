                <h3 class="post-title">Every Angel Carbuncular</h3>
                <div class="post-body">
                    <div>
                        <div style="clear:both;">
                        </div>
                        <div>
                            I remind myself, as I reach for my prescription acne cream, of why this is necessary.
                        </div>
                        <div>
                            "You want to have angelically smooth skin, don't you?"
                        </div>
                        <div>
                            I say to my reflection.
                        </div>
                        <div>
                            <br/>
                        </div>
                        <div>
                            She smirks back at me.
                        </div>
                        <div>
                            "Angelically smooth skin?"
                        </div>
                        <div>
                            She nearly laughs while obligingly pointing out the less desirable patches of my face.
                        </div>
                        <div>
                            "You've clearly never seen an angel!"
                        </div>
                        <div>
                            <br/>
                        </div>
                        <div>
                            "No, but I picture them to look a bit like Scott Lauffer,"
                        </div>
                        <div>
                            I say, smearing Tretinoin Gel onto my chin,
                        </div>
                        <div>
                            "Or perhaps Brad Pitt."
                        </div>
                        <div>
                            <br/>
                        </div>
                        <div>
                            "Angels are full of zits, my friend," says my reflection.
                        </div>
                        <div>
                            (She has a strange habit of calling me "friend", though I consider us barely acquaintances.)
                        </div>
                        <div>
                            "And not just their human faces! Why, all four faces of the cherubim are just covered in acne! They're bodies, too, right down to their hoofed feet."
                        </div>
                        <div>
                            <br/>
                            "I've never seen a hoof with acne," I protest.
                        </div>
                        <div>
                            <br/>
                        </div>
                        <div>
                            "Well, you've never seen a cherub!"
                        </div>
                        <div>
                            <br/>
                        </div>
                        <div>
                            "No," I agree, "I haven't."
                        </div>
                        <div>
                            <br/>
                        </div>
                        <div>
                            She pauses for a moment, allowing me to accurately rub the gel into that annoying spot beneath my left eye (her right eye).
                        </div>
                        <div>
                            "Just so. It's true; the eagle face, the bull face, the lion face, the human face: every part of them has acne.
                        </div>
                        <div>
                            Except for their wings, of course.
                        </div>
                        <div>
                            That's why they cover their disgusting pock-marked bodies with them."
                        </div>
                        <div>
                            <br/>
                        </div>
                        <div>
                            It is finished. The lid goes on the cream, the cream goes in the drawer.
                        </div>
                        <div>
                            I rinse the residue off my fingers.
                        </div>
                        <div>
                            I turn off the light,
                        </div>
                        <div>
                            I leave the room.
                        </div>
                        <div>
                            <br/>
                        </div>
                        <div>
                            "All the other angels have tons of acne, too," she shouts to my back.
                        </div>
                        <div>
                            "It's because they never bathe."
                        </div>
                        <div>
                            <br/>
                        </div>
                        <div>
                            I ruminate, as I lie in bed, on what an ugly world this now seems,
                        </div>
                        <div>
                            that I should be so much more lovely than an angel.
                        </div>
