                <h3 class="post-title">Transportation</h3>
                <div class="post-body">
                    <div>
                        <div style="clear:both;">
                        </div>I-80 in Northern New Jersey; 7:23pm in June; 20-something female in a white 2002 Hyundai Sonata:
                        <br/>
                        <br/>
                        Yes, it had cost her 50 dollars to fill her 12-gallon tank and yes, driving faster uses more gas. But she was making more money this summer than she ever had in her life, and a five-hour twice-weekly trip by oneself justifies all sorts of cruelty to the ice caps. Her strategy to not be pulled over: drive a cop-acceptable speed (80 on 80) and wait for someone more daring to fly by. Then follow. Which, except for a traffic jam, had caused the previous 124 miles to reel past at record speed. There were three of them; a forth had fallen by the wayside, perhaps taken their much-anticipated exit or been pulled over. Hard to say. It had been a pickup truck with flames on the back window; now all the three that were left were a black 1980s-style Mustang, a silver SUV-type KIA, and her. They were all in this together; it didn't seem to be a race between them but more a comradery; the signalled to each other to navigate the roads more effectively: "Here, the right-most lane is open!" "Hold up! A wall of slow cars!" "Straightaway and all clear: go ninety-five!" Sometimes she would lead, sometimes the KIA would lead, sometimes the ugly Mustang. No hard feelings, no competition, just a mutual understanding that being there was much better than being here, and that a cop would have trouble pulling over three people at once. Or at least, this is how the situation seemed to Mariah (for that was her name). She thought that perhaps one of her new friends had a radar detector. That would be handy.
                        <br/>
                        <br/>
                        <span style="font-style: italic;"><span style="font-style: italic;"></span></span>I-95 South somewhere between Bridgeport and Westport (closer to Westport); nearly 7:30 in June commuting home; 49-year-old male in black 2006 Ford Explorer:
                        <br/>
                        <br/>
                        The silver Audi had to have been behind him since at least exit 10 on Route 8. At first he hoped it would pass quickly, since it couldn't have been more than half a second behind, but apparently it was content to cruise control with him at 60mph. It was an infuriating speed, really, but Charles wanted to save as much gas as possible, it costing around ninety dollars a week to commute these days. But now he felt that this presence was a friendly one. He had a tendency to feel attached to trucks if he trailed behind them for ten minutes or more, thinking that he and the truck were somehow now pals. After all, trucks are used to being passed, and they would most likely appreciate this vehicle telling them by its patient following and safe distance that "Yes, I like the pace you set." Though the Audi did not give him the safe distance he preferred, he hoped that it thought of his Explorer in kind terms. Oh! What was this? The Audi now drifted into the right lane, out of the slower middle one. Had it decided it wanted to sever their tie, after so long a friendship? Oh, no; this was not the case. It was time for the Audi to take its exit. Goodbye, Audi. Charles waved. He thought perhaps his Explorer did the same.
                        <br/>
                        <br/>
                        I-81 (almost I-78) in Pennsylvania; somewhere between 7:25 and 7:30 (by the analog clock) in June; 31-year old female in a blue 1978 diesel-powered Mercedes:
                        <br/>
                        <br/>
                        It seemed that the middle and left lane played by the familiar rules of four-lane highways, but here in the right lane it worked differently. No one passed. They all seemed to content themselves with moving at the speed of the car in front of them. The Subaru in front of her had been there since shortly out of Harrisburg; the same with the Toyota behind her. Five cars up, the red station wagon had plenty of space in which to accelerate, but it stayed at 50 to 55, and no one seemed to mind. When occasional speedsters would rush angrily into the space in front of the station wagon it felt somehow wrong; like a noisy outsider was disturbing the quietly contented community of the slow lane.  She liked this community-on-the-highway. Had she continued to drive as she characteristically had for 15 years, she would never have noticed. As she exited, whe wondered if the other cars were bidding farewell. She wondered if she would see them again tomorrow.
                        <br/>
                        <br/>
                        White Hall Lane in Belleville; twilight in June; 14-year-old Jacob Zook in his father's open-top buggy:
                        <br/>
                        <br/>
                        The sunset was dissappointing: No golden sky, no pink clouds. But no one could ask for more perfect weather. Intense heat and humidity had sifted away leaving not merely light air, but an atmosphere that felt like it wasn't there at all. The sky itself seemed to push him along, helping him to go in any direction he moved. And the smell! Without a covering on the buggy, he could smell more than musty horse. He filled his lungs with the early summer air repeatedly, joyous in that most basic human instinct. Except for when occasional cars would pass by and he would hold his breath, having been told once years ago that it wasn't good to breathe the fumes. He heard a horse on the other side of the hill and saw an older friend from church, Isaac Yoder, approach over the hill coming towards him, also enjoying the evening in an open-top buggy. "Guten tag, Isaac!" said Jacob. A hesitant car slowed as it navigated the road between the two buggies, and Jacob crossed the street. He and Isaac talked.
