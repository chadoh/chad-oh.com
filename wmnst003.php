                <br />
                <br />
                And so, here you are. All I thought of is a nicely-sized bathtub, a shower with a chair (large enough to comfortably shave your legs in if you so choose), and a toilet with a permanent seat. I can't think of how a woman-centric sink would be any different than our current (apparently man-centered) sinks; or how walls, doors, steps, building shapes, windows, flooring, colors, light-fixtures, floor-plans, city-scapes, city-planning, or anything else in our patriarchal society could possibly be made intrinsically more female-accommodating. Everything but the basics is, I assert, a matter of personal preference, and cannot possibly be made better for all females everywhere in one fell swoop.
