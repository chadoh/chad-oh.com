                <br />
                I found this assignment incredibly difficult. From the very beginning, I recoil at accusations that sky-scrapers have more to do with penis-worship than simple efficiency: people all want to live in one condensed place, so we build up, rather than sprawl out. Doors aren't yonic symbols, they're the only means of closing off walls. At least limited I cannot think of another means. And surely walls aren't themselves a contraption dreamt-up by the patriarchy to keep women down! If one would assert so, I would think they would also have to argue that privacy itself is an invention of the patriarchy. "A woman-run world would have no walls and we would all copulate in the open." This seems like absolute nonsense to me.
                <br />
                <br />
                But for the sake of the assignment, I read the next two paragraphs of the description with the assumption that the first one implied profound truths. But the answers to all the questions seemed obvious and un-insightful. Where do women find the things they need? In department stores, in libraries, in music, in friends, in blank canvases. What do they want? <a title="The same thing all humans want" href="http://en.wikipedia.org/wiki/Hierarchy_of_human_needs" id="ae22">The same thing all humans want</a>. Where/how do they dwell? In houses, in huts, in mansions, in boxes! The same places as men, and in which I already stated I see no genderization. How do women move from place to place? They walk, they bike, they drive, they bus, they train, they fly. None of these provided any insight into how a place designed explicitly for women would be any different than a place built explicitly for men. That is, unless I espoused gross stereotypes as definitive: "women like pink things and soft things".
                <br />
                <br />
                After several hours of contemplation and discussion with male and female friends, all I could come up with that would definitely be different in a woman-centric world is that toilets would not have movable seats, and bathtubs would perhaps be large enough to birth in. But woe is me, for upon reading her extended instructions, I found that I should create "a small village, or a[n entire] building" that "would eliminate...problems that we face...today," and would make life "very different." But I argue vehemently against the notion that our architecture has any significant impact on the rampant sexism in our society when compared to bad education and media entrenchment (isn't that just more education?). 
                <br />
                <br />
