            <!--begin .post--><h3 class="post-title" id="modernist-poems">Modernist Poetry</h3>
            <div class="post-body">
            	<p>The first task was to write three poems, one about an every-day object, one about a food, and one about a 
				room. These were to be in the style of <a href="http://www.bartleby.com/140/">Gertrude Stein</a>. Here's what 
				I came up with:</p>
				<h4>Boxes for Ticky Tacky</h4>
				<p>Packing in dreams we're packing and moving rooms and apartments and lives now but to where. Fog has 
				overtaken the train hit the impending fog but the sun never cleared it and now </p>
				<p>Floating through fog with Boxes with Things of Memories of Necessities of Shit. Not much of that 
				and hard to tell which. But difficult to remove any and still feel at home there. Where someday we'll 
				know. </p>
				<h4>Au Naturel Jif</h4>
				<p>Brown's more authentic than red and so is stirring but on the label clearly is No Stirring Required! 
				Still only five tributaries none of which with hydrogen, plus molasses. Molasses and molasses and molasses, 
				last but first, engorging and stiffening and surely responsible for familiar consistencies, familiar processes: 
