                <br /><br/>
                <div style="margin-left: 20px;">
                    ...the women's world assignment is a bit free-flowing just to try to
                    prevent you from feeling restricted in how you approach it, though I
                    apologize if that makes it unclear.&nbsp; Basically, your task is to imagine
                    a space that would be ideal for women, in that it would eliminate some
                    of the problems that we face in our real world context today.&nbsp; The
                    space you design (say, a small village, or a building) should show how
                    life would be different in this world you've imagined--that's the
