            	<p>Film stills, inspired by <a href="http://www.moma.org/interactives/exhibitions/1997/sherman/index.html">Cindy Sherman</a>.
				I am to describe the film in which I star, why I wrote it, 
				and how successful the images are at interrogating notions of femininity, the gaze, and spectatorship.</p>
				<br /><a href="images/filmstills/Bumming.png"><img title="The Poster of Homelessness" alt="Picture of bum being rejected in his quest for money" src="images/filmstills/BummingSmall.PNG" /></a>
				<p>In this film I depict a bum asking for money. I do not get money.</p>
				</p>Honestly, I just had fun making these film stills, but basically all of them ended up portraying men who seem 
				to have no skills or gumption. Which is how men are portrayed in pop culture, for the most part. From Homer
				Simpson to every Adam Sandler and Will Ferrell character, to the dolts in The Hangover.</p>
				<br /><a href="images/filmstills/FeedingSquirrels.png"><img title="Feeding Squirrels instead of Learning" alt="Reaching hands-full-of-bird-seed towards squirrels, off-camera" src="images/filmstills/FeedingSquirrelsSmall.PNG" /></a>
				<p>In this film I am depicted reaching hands full of bird seed towards squirrels. There were actual squirrels right off "screen", who unfortunately
				did not see fit to come close enough to be in the shot. Insofar as the entire allure of spectatorship is built
				upon seeing others do things that we may do but cannot observe ourselves doing, I would say it fits the criteria
				of interrogating the notion of spectatorship. Also, it perhaps shows an example of an activity that most of
				us are usually far too sadly busy to enjoy.</p>
				<br /><a href="images/filmstills/CigaretteHaver.png"><img title="A Dapper-Looking Man with a Cigarette" alt="He sits in a jazz-club, looking surprised" src="images/filmstills/CigaretteHaverSmall.PNG"/></a>
				<p>This still is from a film in which this dapper bloke attempts to enjoy a cigarette in a jazz club and is 
				bugged by the fellow in the next image. This character seems like he might be the only character I've dreamt
				up who has a job and gets along well in society. But truth be told, I'm not even sure about that. The 
				characters we may most want to emulate are inaccessible and quirky.</p>
				<br /><a href="images/filmstills/CigaretteWanter.png"><img title="A Dirty-Looking Man Slouching Wantingly" alt="He sits in a jazz-club, looking dirty" src="images/filmstills/CigaretteWanterSmall.PNG"/></a>
				<p>This character falls much more in-line with all the others I've created, again playing off of traditional
				depictions of visionless, pitiful men in popular culture. I find it interesting, though, that while we who 
				take women's studies courses think about these things and somehow differentiate that Cindy Sherman's and my 
				works are illuminating the stereotypes that popular culture so strongly reinforces, to everyone else it may
				simply seem that we are further reinforcing them ourselves. Have I done so by setting up these films? Perhaps.</p>
				<br /><a href="images/filmstills/FumanchuSwing.png"><img title="Well, Ah Dohn No." alt="A man with ugly facial hair stands beneath a leafless tree beneath a white sky, holding a swing." src="images/filmstills/FumanchuSwingSmall.PNG"/></a>
				<p>This character is out-of-touch with current style, but not on purpose for the sake of irony. He honestly
				thinks that the fumanchu is a trendy lady-catcher. He does tricks on <del>his</del> a child's swing, hoping
				to woo on-lookers. No one comes close.</p>
				<p>It's fun to dream up worthless characters. Perhaps for the same reason Cindy Sherman found it fun to dream
				up female clichés for her film stills ("She stopped, she has explained, when she ran out of clichés"). Though
				I do question the ability this sort of commentary has to cause spectators to ponder the stereotypes they 
				depict. I know I just generally get involved in the clichés the same way I do with actual pop-culture.</p> 
