				<h3 class="post-title" id="lettersReflections">Letters Project Reflections</h3>
				<p>I suspect that you may find it interesting to know that I much more enjoyed adding to someone else's story than I did writing my own. Perhaps it was the mood I am in today as opposed to last week, but I found creating my own characters to be a terrible dilemna. I was not very inspired. I made the female-me basically like the real (male) me for ease of creation, and I deliberated for an hour or more which female character from pop culture to use. I settled on Juno MacGuff because I feel like I can use her vocabulary easily, and I felt like I knew a decent bit about her. After writing as her, though, I realized that I knew very little. We draw from an overwhelming resevoir of experience when we write anything, and to write as another person is a difficult task indeed.</p>
				<p>However, when I looked at Shawn Taylor's writings, he already created plot! And so all I had to do was flesh it out. I knew enough about Frida Kahlo to speak fairly confidently as her (in a "I'm imagining what Frida would be like if she were alive today, and also if she were me!" kind of way), and the story/character-development just sort of flowed. It was much easier than writing my story, for which I also needed to create some semblance of plot (though I attempted to barely make one, in the post-modern tradition of denying that there are any story-lines).</p>
				<p>I've said this before, but I always try to see a world without gender. I fail immeasurably, to be sure, but I don't see what role gender plays, other than the fact that I have never been a woman and therefore it is more difficult to write as if I am one. I kept imagining "Shawntay," in the story I was adding to, was a man, that the character was the creator, Shawn. Likewise, in my story, I kept forgetting that the character of me was in fact a female version of me. But! I insist that it shouldn't matter. (That is, I feel slightly silly admitting that my dream was to be a rockstar, a writer, and an inventor, and were I female I would hope I would feel no less and no more silly for wanting these things.)</p>
				<p>As far as having other's write my story: I found this very strange. I thought it would be more fun that it was. Some of the results were fabulous, but at other times the characters said things that I, their creator!, could never see them saying. "That's not what she would say!!" I exclaimed. However, though when I was writing originally and kept forgetting that the "me" character was a female, coming back to the story after other's interventions made that a much more difficult thing to forget. I was much more convinced that Erica was a female after others added to it.</p>
				<p>(I suppose this speaks to the way we identify ourselves by what others say of us--I find it much more easy to work with a character once others have also given her definition. It is probably true of ourselves, also. Being fairly uncertain of what our character (our self) ought to be like, we take what other's say about our selves as our own definition, and expound on it from there. Occasionally, they'll say things about our selves that we know we definitely are not, but this to also serves to aid our self-construction: before they said it, we were more nebulous. Now we have something to revolt against, a corner of our self that we can securely, knowingly lean on.)</p>
